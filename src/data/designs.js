// Helper function to get design by index
export const getDesignByIndex = (index) => {
  return designs.find(design => design.index === parseInt(index));
};

// Helper function to search designs
export const searchDesigns = (query, filters = {}) => {
  let results = designs.filter(design => {
    // Text search across all fields
    const searchText = query.toLowerCase();
    const matchesText = !query ||
      design.design_name.toLowerCase().includes(searchText) ||
      design.barrel_style.toLowerCase().includes(searchText) ||
      design.barrel_length.toLowerCase().includes(searchText) ||
      design.frame_grip_material.toLowerCase().includes(searchText) ||
      design.grip_style.toLowerCase().includes(searchText) ||
      design.release_date.toLowerCase().includes(searchText) ||
      design.drop_cycle.toLowerCase().includes(searchText);

    // Filter by specific characteristics
    const matchesBarrelStyle = !filters.barrel_style || design.barrel_style === filters.barrel_style;
    const matchesBarrelLength = !filters.barrel_length || design.barrel_length === filters.barrel_length;
    const matchesFrameGripMaterial = !filters.frame_grip_material || design.frame_grip_material === filters.frame_grip_material;
    const matchesGripStyle = !filters.grip_style || design.grip_style === filters.grip_style;
    const matchesDropCycle = !filters.drop_cycle || design.drop_cycle === filters.drop_cycle;

    return matchesText && matchesBarrelStyle && matchesBarrelLength && matchesFrameGripMaterial && matchesGripStyle && matchesDropCycle;
  });

  return results;
};

// Get unique values for filter dropdowns
export const getUniqueValues = (field) => {
  const values = designs
    .map(design => design[field])
    .filter(value => value && value.trim() !== '')
    .sort();
  return [...new Set(values)];
};